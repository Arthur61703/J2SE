package exercise;

import java.awt.image.BufferedImage;

//import java.util.*;

public class E2 {
	
	/***一个图片基本的5个要素***/
	protected BufferedImage m;
	protected int x;
	protected int y;
	protected int width;
	protected int height;
	
	public BufferedImage getM() {
		return m;
	}
	public void setM(BufferedImage m) {
		this.m = m;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}

}
